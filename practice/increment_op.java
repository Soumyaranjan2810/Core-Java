public class increment_op {
    public static void main(String[] args) {
        int x= 10;
        System.out.println(x);
        x=++x;
        System.out.println(x);
        x=x++;          //....self assignment
        System.out.println(x);
        x=x++;
        System.out.println(x);
        x=x++;
        System.out.println(x);
        x=x++;
        System.out.println(x);
    }
}